    private void jToggleButtonDelCliActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButtonDelCliActionPerformed
        int sel = jComboBoxClientes.getSelectedIndex();
        if (sel < 0) {
            JOptionPane.showMessageDialog(this, "Selecciona un cliente.");
            return;
        }
        Cliente c = listaClientes.get(sel);
        repoClientes.eliminar(c.getIdCliente());
        JOptionPane.showMessageDialog(this,
                "Cliente “" + c.getNombreCliente() + "” eliminado.");
        limpiarCampos();
        cargarListadoClientes();
    }//GEN-LAST:event_jToggleButtonDelCliActionPerformed

    private void jToggleButtonAddCliActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButtonAddCliActionPerformed

        String nombre = jTNombreCli.getText().trim();
        String id = jTNITCli.getText().trim();
        String email = jTEmailCli.getText().trim();

        if (nombre.isEmpty() || id.isEmpty() || email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Completa todos los campos.");
            return;
        }
        if (repoClientes.obtener(id) != null) {
            JOptionPane.showMessageDialog(this,
                    "El NIT ya existe: " + id,
                    "Aviso",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        Cliente nuevo = new Cliente(nombre, id, email);
        repoClientes.agregar(nuevo);
        JOptionPane.showMessageDialog(this, "Cliente agregado con éxito.");
        limpiarCampos();
        cargarListadoClientes();
    }//GEN-LAST:event_jToggleButtonAddCliActionPerformed

    private void jComboBoxClientesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBoxClientesActionPerformed
        int sel = jComboBoxClientes.getSelectedIndex();
        if (sel >= 0) {
            Cliente c = listaClientes.get(sel);
            jTNombreCli.setText(c.getNombreCliente());
            jTNITCli.setText(c.getIdCliente());
            jTEmailCli.setText(c.getEmailCliente());
        }
    }//GEN-LAST:event_jComboBoxClientesActionPerformed

    private void jButtonActualizarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonActualizarClienteActionPerformed
        String nombre = jTNombreCli.getText().trim();
        String id = jTNITCli.getText().trim();
        String email = jTEmailCli.getText().trim();

        if (nombre.isEmpty() || id.isEmpty() || email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Completa todos los campos.");
            return;
        }
        Cliente existente = repoClientes.obtener(id);
        if (existente == null) {
            JOptionPane.showMessageDialog(this,
                    "No hay cliente registrado con NIT " + id,
                    "Aviso",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }
        Cliente actualizado = new Cliente(nombre, id, email);
        ((GestionClientes) repoClientes).actualizarCliente(actualizado);
        JOptionPane.showMessageDialog(this,
                "Cliente actualizado con éxito.");
        limpiarCampos();
        cargarListadoClientes();
    }//GEN-LAST:event_jButtonActualizarClienteActionPerformed

